/** \brief funcion para dividir dos numeros ingresados
 *
 * \param numeroA float almacena numero uno ingresado por usuario
 * \param numeroB float almacena numero dos ingresado por usuario
 * \return float devuelve la multiplicacion de dos variables
 *
 */
float dividir(float numeroA,float numeroB);

/** \brief funcion para factoriar numero ingresado
 *
 * \param numeroA float almacena numero uno ingresado por usuario
 * \return float devuelve el factorial del numero ingresado
 *
 */
float factoriar(float numeroA);
