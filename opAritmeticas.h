/** \brief funcion para sumar dos numeros ingresados
 *
 * \param numeroA int almacena numero uno ingresado por usuario
 * \param numeroB int almacena numero dos ingresado por usuario
 * \return float devuelve la suma de las dos variables
 *
 */
